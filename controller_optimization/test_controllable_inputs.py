def test_policy_output_dimensions():
    """Test that policy output dimensions are correctly calculated."""

    print("\n" + "="*70)
    print("TEST: Policy Output Dimensions")
    print("="*70)

    for process in PROCESSES:
        info = get_controllable_info(process)
        full_dim = len(process['input_labels'])
        controllable_dim = info['n_controllable']

        print(f"\n{process['name']}:")
        print(f"  Full input dim: {full_dim}")
        print(f"  Policy output dim: {controllable_dim}")
        print(f"  Reduction: {full_dim - controllable_dim} dimensions saved")

        # Verify dimensions match
        assert controllable_dim == len(info['controllable_labels'])
        assert controllable_dim + info['n_non_controllable'] == full_dim

    print("\n✓ TEST PASSED: All dimensions correctly calculated!")


