#!/usr/bin/env python3
"""
Convert full_trajectories.pt to the format required by a chosen CausalIT model.

Reads trajectories generated by generate_dataset.py and converts them to:
- ProT format: train_X.npy, train_Y.npy  (X concatenates [inputs, outputs] per process)
- StageCausaliT / SingleCausalLayer format: train_ds.npz with keys {s, x, y}

Usage:
    python convert_dataset.py [--trajectories_path PATH] [--output_dir PATH]
"""

import sys
import json
import argparse
from pathlib import Path
import numpy as np
import torch

REPO_ROOT = Path(__file__).parent.parent.parent
sys.path.insert(0, str(REPO_ROOT))

from configs.surrogate_config import SURROGATE_CONFIG


def convert_trajectories_to_causalit_format(
    trajectories_path: str,
    output_dir: str,
    model_type: str,
    train_frac: float = 0.7,
    val_frac: float = 0.15,
    test_frac: float = 0.15,
    seed: int = 42,
) -> dict:
    """
    Convert full_trajectories.pt to the format required by the chosen model.

    Args:
        trajectories_path: Path to full_trajectories.pt
        output_dir: Directory to save converted data
        model_type: One of 'proT', 'StageCausaliT', 'SingleCausalLayer'
        train_frac: Fraction of data for training
        val_frac: Fraction of data for validation
        test_frac: Fraction of data for testing
        seed: Random seed for splitting

    Returns:
        dict with statistics about converted data
    """
    assert abs(train_frac + val_frac + test_frac - 1.0) < 1e-6, \
        f"Fractions must sum to 1.0, got {train_frac + val_frac + test_frac}"

    # Load trajectories
    print(f"Loading trajectories from {trajectories_path}...")
    trajectories = torch.load(trajectories_path, map_location='cpu', weights_only=False)
    n_samples = len(trajectories)
    print(f"  Loaded {n_samples} trajectories")

    # Determine process order from the first trajectory
    process_names = list(trajectories[0]['trajectory'].keys())
    print(f"  Processes: {process_names}")

    # Extract data based on model type
    var_metadata = None
    if model_type == 'proT':
        X, Y = _format_prot(trajectories, process_names)
    elif model_type in ('StageCausaliT', 'SingleCausalLayer'):
        S, Xint, Y, var_metadata = _format_stage_causal(trajectories, process_names)
    else:
        raise ValueError(f"Unknown model_type: {model_type}. "
                         f"Expected 'proT', 'StageCausaliT', or 'SingleCausalLayer'")

    # Train/val/test split
    rng = np.random.RandomState(seed)
    indices = rng.permutation(n_samples)

    n_train = int(n_samples * train_frac)
    n_val = int(n_samples * val_frac)

    train_idx = indices[:n_train]
    val_idx = indices[n_train:n_train + n_val]
    test_idx = indices[n_train + n_val:]

    print(f"  Split: train={len(train_idx)}, val={len(val_idx)}, test={len(test_idx)}")

    # Save
    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)

    if model_type == 'proT':
        _save_prot(X, Y, train_idx, val_idx, test_idx, output_path)
    else:
        _save_stage_causal(S, Xint, Y, train_idx, val_idx, test_idx, output_path,
                           var_metadata, process_names)

    # Compute statistics (value is always at column 0 for stage-causal format)
    if model_type in ('StageCausaliT', 'SingleCausalLayer'):
        F_all = Y[:, :, 0].flatten()
    else:
        F_all = Y.flatten()
    stats = {
        'n_samples': n_samples,
        'n_train': len(train_idx),
        'n_val': len(val_idx),
        'n_test': len(test_idx),
        'model_type': model_type,
        'F_mean': float(np.mean(F_all)),
        'F_std': float(np.std(F_all)),
    }

    print(f"  F statistics: mean={stats['F_mean']:.4f}, std={stats['F_std']:.4f}")
    print(f"  Saved to: {output_path}")

    return stats


def _format_prot(trajectories, process_names):
    """
    Format for ProT (TransformerForecaster).

    For each trajectory: concatenate [inputs, env, outputs, var=0] per process.
    var is zero because SCM data is deterministic given the seed.
    Features are zero-padded to the max dimension across processes.

    Returns:
        X: np.array (N, n_processes, features)
        Y: np.array (N, 1, 1)
    """
    n_samples = len(trajectories)
    n_processes = len(process_names)

    # First pass: determine max feature dim
    sample = trajectories[0]['trajectory']
    feature_dims = []
    for pname in process_names:
        inp = sample[pname]['inputs']
        env = sample[pname].get('env')
        out = sample[pname]['outputs']
        inp_dim = inp.numel() if isinstance(inp, torch.Tensor) else np.array(inp).size
        env_dim = (env.numel() if isinstance(env, torch.Tensor) else np.array(env).size) if env is not None else 0
        out_dim = out.numel() if isinstance(out, torch.Tensor) else np.array(out).size
        feature_dims.append(inp_dim + env_dim + out_dim + out_dim)
    max_features = max(feature_dims)

    # Debug: show per-process feature structure
    print(f"  ProT format: [inputs | env | outputs | var=0]")
    print(f"  max_features={max_features} (zero-padded)")
    for pname, fdim in zip(process_names, feature_dims):
        s = sample[pname]
        inp = s['inputs']
        env = s.get('env')
        out = s['outputs']
        inp_d = inp.numel() if isinstance(inp, torch.Tensor) else np.array(inp).size
        env_d = (env.numel() if isinstance(env, torch.Tensor) else np.array(env).size) if env is not None else 0
        out_d = out.numel() if isinstance(out, torch.Tensor) else np.array(out).size
        layout = f"inp={inp_d}"
        if env_d > 0:
            layout += f" + env={env_d}"
        layout += f" + out={out_d} + var={out_d}"
        print(f"    {pname}: [{layout}] = {fdim} features"
              f"{' (padded to ' + str(max_features) + ')' if fdim < max_features else ''}")

    # Build arrays
    X = np.zeros((n_samples, n_processes, max_features), dtype=np.float32)
    Y = np.zeros((n_samples, 1, 1), dtype=np.float32)

    for i, traj_data in enumerate(trajectories):
        traj = traj_data['trajectory']
        for j, pname in enumerate(process_names):
            inp = _to_numpy_flat(traj[pname]['inputs'])
            parts = [inp]
            env = traj[pname].get('env')
            if env is not None:
                parts.append(_to_numpy_flat(env))
            out = _to_numpy_flat(traj[pname]['outputs'])
            parts.append(out)
            parts.append(np.zeros_like(out))
            features = np.concatenate(parts)
            X[i, j, :len(features)] = features
        Y[i, 0, 0] = traj_data['F']

    return X, Y


def _format_stage_causal(trajectories, process_names):
    """
    Format for StageCausaliT / SingleCausalLayer.

    Each input/output scalar becomes a separate token with [value, variable_id].
    Variable IDs use shared (global) numbering:
        S variables: 1 .. n_s
        X variables: (n_s+1) .. (n_s+n_x)
        Y variables: (n_s+n_x+1) .. (n_s+n_x+n_y)

    This matches the SCM dataset format expected by StageCausaliT.

    Returns:
        S: np.array (N, n_s, 2)     - source tokens [value, variable_id]
        X: np.array (N, n_x, 2)     - intermediate tokens [value, variable_id]
        Y: np.array (N, n_y, 2)     - target tokens [value, variable_id]
        var_metadata: dict           - variable counts and labels
    """
    n_samples = len(trajectories)

    # First pass: count total variables per stage
    sample = trajectories[0]['trajectory']
    s_dims_per_process = []
    x_dims_per_process = []
    for pname in process_names:
        inp = _to_numpy_flat(sample[pname]['inputs'])
        out = _to_numpy_flat(sample[pname]['outputs'])
        s_dims_per_process.append(len(inp))
        x_dims_per_process.append(len(out))

    n_s = sum(s_dims_per_process)   # total source variables
    n_x = sum(x_dims_per_process)   # total intermediate variables
    n_y = 1                         # F is a single target

    # Build arrays: (N, seq_len, 2) with [value, variable_id]
    S = np.zeros((n_samples, n_s, 2), dtype=np.float32)
    X = np.zeros((n_samples, n_x, 2), dtype=np.float32)
    Y = np.zeros((n_samples, n_y, 2), dtype=np.float32)

    # Build variable labels for metadata
    s_labels = []
    x_labels = []
    s_var_id = 1
    x_var_id = n_s + 1
    for pname in process_names:
        n_inp = s_dims_per_process[process_names.index(pname)]
        n_out = x_dims_per_process[process_names.index(pname)]
        for k in range(n_inp):
            s_labels.append(f"{pname}_in{k}")
        for k in range(n_out):
            x_labels.append(f"{pname}_out{k}")
    y_labels = ["F"]

    for i, traj_data in enumerate(trajectories):
        traj = traj_data['trajectory']
        s_idx = 0
        x_idx = 0
        for pname in process_names:
            inp = _to_numpy_flat(traj[pname]['inputs'])
            out = _to_numpy_flat(traj[pname]['outputs'])
            for val in inp:
                S[i, s_idx] = [val, s_idx + 1]           # 1-indexed
                s_idx += 1
            for val in out:
                X[i, x_idx] = [val, n_s + x_idx + 1]    # offset by n_s
                x_idx += 1
        Y[i, 0] = [traj_data['F'], n_s + n_x + 1]

    var_metadata = {
        'n_source': n_s,
        'n_input': n_x,
        'n_target': n_y,
        'source_labels': s_labels,
        'input_labels': x_labels,
        'target_labels': y_labels,
        's_dims_per_process': s_dims_per_process,
        'x_dims_per_process': x_dims_per_process,
    }

    print(f"  Stage-causal format: S({n_s} vars), X({n_x} vars), Y({n_y} var)")
    return S, X, Y, var_metadata


def _to_numpy_flat(t):
    """Convert a tensor or scalar to a flat numpy array."""
    if isinstance(t, torch.Tensor):
        return t.detach().cpu().numpy().flatten()
    return np.atleast_1d(np.array(t, dtype=np.float32)).flatten()


def _save_prot(X, Y, train_idx, val_idx, test_idx, output_path):
    """Save ProT-format data as .npy files."""
    for split_name, idx in [('train', train_idx), ('val', val_idx), ('test', test_idx)]:
        np.save(output_path / f'{split_name}_X.npy', X[idx])
        np.save(output_path / f'{split_name}_Y.npy', Y[idx])

    # Save metadata for compatibility with existing SurrogateTrainer
    np.savez(output_path / 'train_metadata.npz',
             n_samples=len(train_idx),
             source='full_trajectories.pt')

    print(f"  Saved ProT format: {{split}}_X.npy, {{split}}_Y.npy")


def _save_stage_causal(S, X, Y, train_idx, val_idx, test_idx, output_path,
                       var_metadata, process_names):
    """Save StageCausaliT/SingleCausalLayer format as .npz files + metadata."""
    # Save combined file (used by Lightning trainer with k-fold)
    np.savez(output_path / 'ds.npz', s=S, x=X, y=Y)

    # Also save per-split files for reference / pre-split mode
    # Combine train+val for Lightning trainer (k-fold handles the val split)
    train_val_idx = np.concatenate([train_idx, val_idx])
    np.savez(output_path / 'train_ds.npz',
             s=S[train_val_idx], x=X[train_val_idx], y=Y[train_val_idx])
    np.savez(output_path / 'test_ds.npz',
             s=S[test_idx], x=X[test_idx], y=Y[test_idx])

    print(f"  Saved stage-causal format: ds.npz, train_ds.npz, test_ds.npz")

    # Save dataset_metadata.json (required by Lightning trainer)
    dataset_metadata = {
        'name': 'surrogate_stage_causal',
        'description': f'Stage-causal data from process chain trajectories ({", ".join(process_names)})',
        'variable_info': {
            'n_source': var_metadata['n_source'],
            'n_input': var_metadata['n_input'],
            'n_target': var_metadata['n_target'],
            'source_labels': var_metadata['source_labels'],
            'input_labels': var_metadata['input_labels'],
            'target_labels': var_metadata['target_labels'],
        },
        'feature_indices': {
            'value': 0,
            'variable': 1,
        },
    }
    with open(output_path / 'dataset_metadata.json', 'w') as f:
        json.dump(dataset_metadata, f, indent=2)
    print(f"  Saved dataset_metadata.json")


def main():
    parser = argparse.ArgumentParser(
        description='Convert full_trajectories.pt to CausalIT training format')
    parser.add_argument('--trajectories_path', type=str,
                        default=None,
                        help='Path to full_trajectories.pt')
    parser.add_argument('--output_dir', type=str,
                        default='causaliT/data/surrogate_training',
                        help='Output directory for converted data')
    parser.add_argument('--model_type', type=str, default=None,
                        help='Model type: proT, StageCausaliT, SingleCausalLayer '
                             '(default: from surrogate_config)')
    parser.add_argument('--train_frac', type=float, default=None)
    parser.add_argument('--val_frac', type=float, default=None)
    parser.add_argument('--test_frac', type=float, default=None)
    parser.add_argument('--seed', type=int, default=42)
    args = parser.parse_args()

    config = SURROGATE_CONFIG

    # Resolve defaults from config
    model_type = args.model_type or config['model'].get('casualit_model', 'proT')
    trajectories_path = args.trajectories_path or config['data'].get(
        'dataset_path', 'data/trajectories/full_trajectories.pt')
    train_frac = args.train_frac or config['data'].get('train_frac', 0.70)
    val_frac = args.val_frac or config['data'].get('val_frac', 0.15)
    test_frac = args.test_frac or config['data'].get('test_frac', 0.15)

    print("=" * 70)
    print("CasualiT Dataset Conversion")
    print("=" * 70)
    print(f"  Model type: {model_type}")
    print(f"  Source: {trajectories_path}")
    print(f"  Output: {args.output_dir}")

    stats = convert_trajectories_to_causalit_format(
        trajectories_path=trajectories_path,
        output_dir=args.output_dir,
        model_type=model_type,
        train_frac=train_frac,
        val_frac=val_frac,
        test_frac=test_frac,
        seed=args.seed,
    )

    print("\n" + "=" * 70)
    print("Conversion Complete!")
    print("=" * 70)
    for k, v in stats.items():
        print(f"  {k}: {v}")


if __name__ == '__main__':
    main()
