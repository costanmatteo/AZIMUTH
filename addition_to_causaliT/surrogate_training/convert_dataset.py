def convert_trajectories_to_causalit_format(
    trajectories_path: str,
    output_dir: str,
    model_type: str,
    train_frac: float = 0.7,
    val_frac: float = 0.15,
    test_frac: float = 0.15,
    seed: int = 42,
) -> dict:
    """
    Convert full_trajectories.pt to the canonical [value, var_id] format.

    Args:
        trajectories_path: Path to full_trajectories.pt
        output_dir: Directory to save converted data
        model_type: One of 'proT', 'StageCausaliT', 'SingleCausalLayer'
        train_frac, val_frac, test_frac: split fractions
        seed: Random seed for splitting

    Returns:
        dict with statistics about the converted dataset.
    """
    assert abs(train_frac + val_frac + test_frac - 1.0) < 1e-6, \
        f"Fractions must sum to 1.0, got {train_frac + val_frac + test_frac}"

    print(f"Loading trajectories from {trajectories_path}...")
    trajectories = torch.load(trajectories_path, map_location='cpu', weights_only=False)
    n_samples = len(trajectories)
    print(f"  Loaded {n_samples} trajectories")

    # Determine process order from the first trajectory, then resolve against
    # configs.processes_config so that we can re-order inputs/env into the
    # canonical input_labels order.
    sample_process_names = list(trajectories[0]['trajectory'].keys())
    all_processes = _load_processes_config()
    proc_by_name = {p['name']: p for p in all_processes}
    missing = [n for n in sample_process_names if n not in proc_by_name]
    if missing:
        raise ValueError(
            f"The following processes appear in the trajectories but not in "
            f"configs.processes_config.PROCESSES: {missing}")
    processes_config = [proc_by_name[n] for n in sample_process_names]
    process_names = [p['name'] for p in processes_config]
    print(f"  Processes: {process_names}")

    # Canonical layout (shared with inference-time converter)
    layout = get_canonical_token_layout(processes_config)

    # Extract data based on model type
    if model_type == 'proT':
        X, Y, norm_stats = _format_prot(trajectories, processes_config, layout)
        save_fn = _save_prot
        var_metadata = None
    elif model_type in ('StageCausaliT', 'SingleCausalLayer'):
        S, X, Y, norm_stats, var_metadata = _format_stage_causal(
            trajectories, processes_config, layout)
        save_fn = None
    else:
        raise ValueError(
            f"Unknown model_type: {model_type}. Expected 'proT', "
            f"'StageCausaliT', or 'SingleCausalLayer'")

    # Train/val/test split
    rng = np.random.RandomState(seed)
    indices = rng.permutation(n_samples)
    n_train = int(n_samples * train_frac)
    n_val = int(n_samples * val_frac)
    train_idx = indices[:n_train]
    val_idx = indices[n_train:n_train + n_val]
    test_idx = indices[n_train + n_val:]
    print(f"  Split: train={len(train_idx)}, val={len(val_idx)}, test={len(test_idx)}")

    # Save
    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)

    if model_type == 'proT':
        _save_prot(X, Y, train_idx, val_idx, test_idx, output_path,
                   processes_config, layout, norm_stats)
    else:
        _save_stage_causal(S, X, Y, train_idx, val_idx, test_idx, output_path,
                           processes_config, layout, norm_stats, var_metadata)

    # Compute statistics on F (for reporting only)
    F_all = Y[:, 0, 0].flatten()
    stats = {
        'n_samples': n_samples,
        'n_train': len(train_idx),
        'n_val': len(val_idx),
        'n_test': len(test_idx),
        'model_type': model_type,
        'F_mean_standardized': float(np.mean(F_all)),
        'F_std_standardized': float(np.std(F_all)),
    }
    print(f"  F (standardized): mean={stats['F_mean_standardized']:.4f}, "
          f"std={stats['F_std_standardized']:.4f}")
    print(f"  Saved to: {output_path}")
    return stats


