def _save_prot(X, Y, train_idx, val_idx, test_idx, output_path,
               processes_config, layout, norm_stats):
    """Save ProT-format data + metadata."""
    np.savez(output_path / 'ds.npz', x=X, y=Y)

    for split_name, idx in [('train', train_idx), ('val', val_idx),
                            ('test', test_idx)]:
        np.save(output_path / f'{split_name}_X.npy', X[idx])
        np.save(output_path / f'{split_name}_Y.npy', Y[idx])

    x_tokens = layout['proT']['x_tokens']
    y_tokens = layout['proT']['y_tokens']
    n_x = len(x_tokens)
    n_y = len(y_tokens)

    metadata = {
        'name': 'surrogate_prot',
        'description': (
            'Canonical [value, var_id] tokens for ProT surrogate. '
            'Encoder sequence = inputs + outputs per process in '
            'input_labels/output_labels order.'),
        'model_type': 'proT',
        'variable_info': {
            'n_source': 0,           # ProT has no separate S stream
            'n_input': n_x,          # encoder sequence length
            'n_target': n_y,         # decoder sequence length
            'process_names': [p['name'] for p in processes_config],
            'input_labels': [f"{t['process']}/{t['label']}" for t in x_tokens],
            'target_labels': [t['label'] for t in y_tokens],
        },
        'feature_indices': {'value': 0, 'variable': 1},
        'token_layout': {
            'proT': {
                'x_tokens': x_tokens,
                'y_tokens': y_tokens,
            },
        },
        'normalization': norm_stats,
        'process_dims': layout['process_dims'],
    }
    with open(output_path / 'dataset_metadata.json', 'w') as f:
        json.dump(metadata, f, indent=2)

    print(f"  Saved ProT format: ds.npz, dataset_metadata.json, "
          f"{{split}}_X.npy / {{split}}_Y.npy")


def _save_stage_causal(S, X, Y, train_idx, val_idx, test_idx, output_path,
                       processes_config, layout, norm_stats, var_metadata):
    """Save StageCausaliT/SingleCausalLayer format."""
    np.savez(output_path / 'ds.npz', s=S, x=X, y=Y)

    train_val_idx = np.concatenate([train_idx, val_idx])
    np.savez(output_path / 'train_data.npz',
             s=S[train_val_idx], x=X[train_val_idx], y=Y[train_val_idx])
    np.savez(output_path / 'test_data.npz',
             s=S[test_idx], x=X[test_idx], y=Y[test_idx])

    s_tokens = layout['stage_causal']['s_tokens']
    x_tokens = layout['stage_causal']['x_tokens']
    y_tokens = layout['stage_causal']['y_tokens']

    metadata = {
        'name': 'surrogate_stage_causal',
        'description': (
            'Canonical [value, var_id] tokens for StageCausaliT surrogate. '
            'S = all process inputs, X = all process outputs, Y = F.'),
        'model_type': 'StageCausaliT',
        'variable_info': {
            'n_source': var_metadata['n_source'],
            'n_input': var_metadata['n_input'],
            'n_target': var_metadata['n_target'],
            'source_labels': var_metadata['source_labels'],
            'input_labels': var_metadata['input_labels'],
            'target_labels': var_metadata['target_labels'],
            'process_names': [p['name'] for p in processes_config],
        },
        'feature_indices': {'value': 0, 'variable': 1},
        'token_layout': {
            'stage_causal': {
                's_tokens': s_tokens,
                'x_tokens': x_tokens,
                'y_tokens': y_tokens,
            },
        },
        'normalization': norm_stats,
        'process_dims': layout['process_dims'],
    }
    with open(output_path / 'dataset_metadata.json', 'w') as f:
        json.dump(metadata, f, indent=2)

    print(f"  Saved stage-causal format: ds.npz, train_data.npz, "
          f"test_data.npz, dataset_metadata.json")


