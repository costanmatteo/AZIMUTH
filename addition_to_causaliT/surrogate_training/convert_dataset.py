def _format_prot(trajectories, processes_config, layout):
    """
    Build ProT arrays in the canonical [value, var_id] format.

    For each sample and each process, emits n_inputs + n_outputs tokens in the
    order defined by `layout['proT']['x_tokens']` (input_labels then
    output_labels, one process at a time). var_ids are global 1-based.

    Values (column 0) are then standardized per-variable using statistics
    computed over the full dataset.

    Returns:
        X: (N, n_x_tokens, 2) float32 — standardized values + var_ids
        Y: (N, 1, 2)          float32 — standardized F   + var_id (=1)
        norm_stats: dict describing the standardization applied.
    """
    x_tokens = layout['proT']['x_tokens']
    y_tokens = layout['proT']['y_tokens']
    n_samples = len(trajectories)
    n_x = len(x_tokens)
    n_y = len(y_tokens)

    proc_map = {p['name']: p for p in processes_config}

    # Pre-extract per-process full_inputs and outputs for every sample
    X_raw = np.zeros((n_samples, n_x, 2), dtype=np.float32)
    Y_raw = np.zeros((n_samples, n_y, 2), dtype=np.float32)

    for i, traj_data in enumerate(trajectories):
        traj = traj_data['trajectory']
        # Per-process precompute: full input vector (in input_labels order) + outputs
        per_proc = {}
        for pname, pcfg in proc_map.items():
            if pname not in traj:
                continue
            full_inp = _full_input_vector_from_trajectory(traj[pname], pcfg)
            out = _to_numpy_flat(traj[pname]['outputs'])
            per_proc[pname] = (full_inp, out)

        for k, tok in enumerate(x_tokens):
            full_inp, out = per_proc[tok['process']]
            if tok['kind'] == 'input':
                val = float(full_inp[tok['local_idx']])
            else:
                val = float(out[tok['local_idx']])
            X_raw[i, k, 0] = val
            X_raw[i, k, 1] = tok['var_id']

        Y_raw[i, 0, 0] = float(traj_data['F'])
        Y_raw[i, 0, 1] = y_tokens[0]['var_id']

    # Per-variable standardization (mean/std computed column-wise over samples)
    X, enc_stats = _standardize_per_variable(X_raw)
    Y, dec_stats = _standardize_per_variable(Y_raw)

    norm_stats = {
        'method': 'per_variable',
        'encoder': enc_stats,  # list of {var_id, mean, std}
        'decoder': dec_stats,
    }

    print(f"  ProT format: x=({n_samples}, {n_x}, 2), y=({n_samples}, {n_y}, 2)")
    print(f"  Encoder tokens ({n_x}):")
    for tok in x_tokens:
        print(f"    var_id={tok['var_id']:>3}  "
              f"{tok['process']}/{tok['kind']}/{tok['label']}")
    return X, Y, norm_stats


def _format_stage_causal(trajectories, processes_config, layout):
    """
    Build StageCausaliT arrays in the canonical [value, var_id] format.

    - S collects all process inputs (var_ids 1..n_s, 1-based).
    - X collects all process outputs (var_ids 1..n_x, 1-based).
    - Y holds F with var_id=1.

    Token order follows `layout['stage_causal']`.
    """
    s_tokens = layout['stage_causal']['s_tokens']
    x_tokens = layout['stage_causal']['x_tokens']
    y_tokens = layout['stage_causal']['y_tokens']

    n_samples = len(trajectories)
    n_s = len(s_tokens)
    n_x = len(x_tokens)
    n_y = len(y_tokens)

    proc_map = {p['name']: p for p in processes_config}

    S_raw = np.zeros((n_samples, n_s, 2), dtype=np.float32)
    X_raw = np.zeros((n_samples, n_x, 2), dtype=np.float32)
    Y_raw = np.zeros((n_samples, n_y, 2), dtype=np.float32)

    for i, traj_data in enumerate(trajectories):
        traj = traj_data['trajectory']
        per_proc = {}
        for pname, pcfg in proc_map.items():
            if pname not in traj:
                continue
            full_inp = _full_input_vector_from_trajectory(traj[pname], pcfg)
            out = _to_numpy_flat(traj[pname]['outputs'])
            per_proc[pname] = (full_inp, out)

        for k, tok in enumerate(s_tokens):
            full_inp, _ = per_proc[tok['process']]
            S_raw[i, k, 0] = float(full_inp[tok['local_idx']])
            S_raw[i, k, 1] = tok['var_id']

        for k, tok in enumerate(x_tokens):
            _, out = per_proc[tok['process']]
            X_raw[i, k, 0] = float(out[tok['local_idx']])
            X_raw[i, k, 1] = tok['var_id']

        Y_raw[i, 0, 0] = float(traj_data['F'])
        Y_raw[i, 0, 1] = y_tokens[0]['var_id']

    S, s_stats = _standardize_per_variable(S_raw)
    X, x_stats = _standardize_per_variable(X_raw)
    Y, y_stats = _standardize_per_variable(Y_raw)

    norm_stats = {
        'method': 'per_variable',
        's': s_stats,
        'x': x_stats,
        'y': y_stats,
    }

    var_metadata = {
        'n_source': n_s,
        'n_input': n_x,
        'n_target': n_y,
        'source_labels': [f"{t['process']}/{t['label']}" for t in s_tokens],
        'input_labels': [f"{t['process']}/{t['label']}" for t in x_tokens],
        'target_labels': [t['label'] for t in y_tokens],
    }

    print(f"  Stage-causal format: s=({n_samples},{n_s},2), "
          f"x=({n_samples},{n_x},2), y=({n_samples},{n_y},2)")
    print(f"  S tokens ({n_s}):")
    for tok in s_tokens:
        print(f"    var_id={tok['var_id']:>3}  "
              f"{tok['process']}/{tok['label']}")
    print(f"  X tokens ({n_x}):")
    for tok in x_tokens:
        print(f"    var_id={tok['var_id']:>3}  "
              f"{tok['process']}/{tok['label']}")
    return S, X, Y, norm_stats, var_metadata


# ============================================================================
# Standardization helpers
# ============================================================================


def _standardize_per_variable(arr):
    """
    Standardize column 0 (value) per variable (column 1 = var_id).

    Args:
        arr: np.ndarray of shape (N, L, 2). Column 0 is the raw value,
             column 1 is the integer var_id (assumed identical across samples
             for a given position l).

    Returns:
        standardized: arr copy with column 0 replaced by (value - mean) / std.
        stats: list of dicts [{var_id, mean, std}, ...] in position order.
    """
    out = arr.copy()
    L = arr.shape[1]
    stats = []
    for l in range(L):
        vals = arr[:, l, 0]
        mean = float(vals.mean())
        std = float(vals.std())
        if std < 1e-8:
            std = 1.0
        out[:, l, 0] = (vals - mean) / std
        stats.append({
            'position': l,
            'var_id': int(arr[0, l, 1]),
            'mean': mean,
            'std': std,
        })
    return out, stats


def _to_numpy_flat(t):
    """Convert a tensor or scalar to a flat numpy array."""
    if isinstance(t, torch.Tensor):
        return t.detach().cpu().numpy().flatten().astype(np.float32)
    return np.atleast_1d(np.array(t, dtype=np.float32)).flatten()


