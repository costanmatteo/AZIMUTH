def main():
    parser = argparse.ArgumentParser(
        description='Convert full_trajectories.pt to CausalIT training format')
    parser.add_argument('--trajectories_path', type=str, default=None,
                        help='Path to full_trajectories.pt')
    parser.add_argument('--output_dir', type=str,
                        default='causaliT/data/azimuth_surrogate',
                        help='Output directory for converted data')
    parser.add_argument('--model_type', type=str, default=None,
                        help='Model type: proT, StageCausaliT, SingleCausalLayer '
                             '(default: from surrogate_config)')
