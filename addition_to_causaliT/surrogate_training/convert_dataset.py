#!/usr/bin/env python3
"""
Convert full_trajectories.pt to the format required by a chosen CausalIT model.

Reads trajectories generated by generate_dataset.py and converts them to the
**canonical causaliT token format** [value, variable_id] (2 features per token).

Supported models:
    proT:            x=(N, n_all_vars, 2), y=(N, 1, 2)
                     Each process contributes one token per input variable and
                     one token per output variable, concatenated into a single
                     encoder sequence. var_id is global 1-based.
    StageCausaliT /  s=(N, n_inputs, 2), x=(N, n_outputs, 2), y=(N, 1, 2)
    SingleCausalLayer
                     Inputs go to S, outputs go to X. var_id is 1-based *per
                     array* (s_ids = 1..n_s, x_ids = 1..n_x).

Key invariants (guaranteed by this converter so inference can be aligned):
  1. Feature dim is always 2: [value, variable_id].
  2. variable_id is a 1-based integer (0 is reserved for padding).
  3. Values (column 0) are standardized per-variable (mean=0, std=1) using stats
     computed over the training set and written to dataset_metadata.json.
  4. Token order inside each process is the canonical order returned by
     `get_canonical_token_layout`, which is re-used verbatim by
     `controller.src.core.process_chain.ProcessChain.trajectory_to_prot_format`
     at inference time.

Usage:
    python convert_dataset.py [--trajectories_path PATH] [--output_dir PATH]
"""

import sys
import json
import argparse
from pathlib import Path
import numpy as np
import torch

