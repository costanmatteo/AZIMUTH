# ============================================================================
# Canonical variable / token layout
# ============================================================================
#
# This section is intentionally self-contained and pure. It is imported and
# called by both the training converter and the inference-time converter
# (`ProcessChain.trajectory_to_prot_format`) so that training and inference
# generate *exactly* the same variable ordering and var_ids.
#
# The layout is derived from `configs.processes_config.PROCESSES`, so it is
# stable across runs as long as the process configuration doesn't change.
# ============================================================================


def get_canonical_token_layout(processes_config):
    """
    Build the canonical token layout from a list of process configs.

    Returns a dict with, for each mode ('proT', 'stage_causal'), the ordered
    list of tokens and their var_ids. A token descriptor is a dict:

        {
            'process': <process name>,
            'kind':    'input' | 'output',
            'label':   <variable label>,
            'local_idx': <index of this variable inside its process input/output
                         vector, in input_labels / output_labels order>,
            'var_id':  <1-based integer ID used by the surrogate>,
        }

    Rules:
      - Token order inside a process = input_labels order, then output_labels
        order. This matches the layout of `trajectory[pname]['inputs']` /
        `['outputs_sampled']` produced by `ProcessChain.forward` (which writes
        the full merged input vector in input_labels order).
      - ProT: single global sequence (inputs and outputs interleaved per
        process), global 1-based var_id.
      - StageCausaliT: two separate sequences. S contains all inputs
        (var_id 1..n_s, 1-based), X contains all outputs (var_id 1..n_x,
        1-based). Y has a single token with var_id = 1.

    Args:
        processes_config: list of process dicts, each with keys 'name',
            'input_labels', 'output_labels'.

    Returns:
        dict with keys:
            'proT':         {'x_tokens': [...], 'y_tokens': [...]}
            'stage_causal': {'s_tokens': [...], 'x_tokens': [...],
                             'y_tokens': [...]}
            'process_dims': list of {'name', 'n_inputs', 'n_outputs'}
    """
    process_dims = []
    for pc in processes_config:
        process_dims.append({
            'name': pc['name'],
            'n_inputs': len(pc['input_labels']),
            'n_outputs': len(pc['output_labels']),
            'input_labels': list(pc['input_labels']),
            'output_labels': list(pc['output_labels']),
        })

    # --- ProT layout: single global sequence -----------------------------
    prot_x_tokens = []
    gid = 1
    for pd in process_dims:
        for local_idx, label in enumerate(pd['input_labels']):
            prot_x_tokens.append({
                'process': pd['name'],
                'kind': 'input',
                'label': label,
                'local_idx': local_idx,
                'var_id': gid,
            })
            gid += 1
        for local_idx, label in enumerate(pd['output_labels']):
            prot_x_tokens.append({
                'process': pd['name'],
                'kind': 'output',
                'label': label,
                'local_idx': local_idx,
                'var_id': gid,
            })
            gid += 1
    prot_y_tokens = [{
        'process': '__target__', 'kind': 'target', 'label': 'F',
        'local_idx': 0, 'var_id': 1,
    }]

    # --- StageCausaliT layout: separate S and X sequences ----------------
    sc_s_tokens = []
    s_id = 1
    for pd in process_dims:
        for local_idx, label in enumerate(pd['input_labels']):
            sc_s_tokens.append({
                'process': pd['name'],
                'kind': 'input',
                'label': label,
                'local_idx': local_idx,
                'var_id': s_id,
            })
            s_id += 1
    sc_x_tokens = []
    x_id = 1
    for pd in process_dims:
        for local_idx, label in enumerate(pd['output_labels']):
            sc_x_tokens.append({
                'process': pd['name'],
                'kind': 'output',
                'label': label,
                'local_idx': local_idx,
                'var_id': x_id,
            })
            x_id += 1
    sc_y_tokens = [{
        'process': '__target__', 'kind': 'target', 'label': 'F',
        'local_idx': 0, 'var_id': 1,
    }]

    return {
        'proT': {'x_tokens': prot_x_tokens, 'y_tokens': prot_y_tokens},
        'stage_causal': {
            's_tokens': sc_s_tokens,
            'x_tokens': sc_x_tokens,
            'y_tokens': sc_y_tokens,
        },
        'process_dims': process_dims,
    }


def _load_processes_config():
    """Load PROCESSES from configs.processes_config (lazy to avoid circular imports)."""
    from configs.processes_config import PROCESSES
    return PROCESSES


def _get_controllable_split(process_config):
    """Return (controllable_indices, non_controllable_indices) for a process."""
    from configs.processes_config import get_controllable_inputs
    input_labels = process_config['input_labels']
    controllable = get_controllable_inputs(process_config)
    ctrl_idx, non_ctrl_idx = [], []
    for i, lab in enumerate(input_labels):
        if lab in controllable:
            ctrl_idx.append(i)
        else:
            non_ctrl_idx.append(i)
    return ctrl_idx, non_ctrl_idx


def _full_input_vector_from_trajectory(traj_per_process, process_config):
    """
    Reconstruct the *full* input vector of a process in `input_labels` order
    from an offline trajectory entry that stores `inputs` (controllable only)
    and `env` (non-controllable only) as separate arrays.

    This matches the layout that `ProcessChain._merge_controllable_inputs`
    produces at inference time, so training and inference agree on token order.
    """
    input_dim = len(process_config['input_labels'])
    ctrl_idx, non_ctrl_idx = _get_controllable_split(process_config)

    control = _to_numpy_flat(traj_per_process['inputs'])
    env_raw = traj_per_process.get('env')
    env = _to_numpy_flat(env_raw) if env_raw is not None else np.zeros(0, dtype=np.float32)

    if len(control) != len(ctrl_idx):
        raise ValueError(
            f"Process '{process_config['name']}': stored 'inputs' has "
            f"{len(control)} values but process config declares "
            f"{len(ctrl_idx)} controllable inputs.")
    if len(env) != len(non_ctrl_idx):
        raise ValueError(
            f"Process '{process_config['name']}': stored 'env' has "
            f"{len(env)} values but process config declares "
            f"{len(non_ctrl_idx)} non-controllable inputs.")

    full = np.zeros(input_dim, dtype=np.float32)
    for out_i, pos in enumerate(ctrl_idx):
        full[pos] = control[out_i]
    for out_i, pos in enumerate(non_ctrl_idx):
        full[pos] = env[out_i]
    return full


